package Language_Fundamentals;

public class SpacingMethodsInJava {

	public static void main(String[] args) {
		
		String str = "shubham";
		
		System.out.println("Righht spacce ");
	
		
		System.out.printf("-------------->%-15s",str);
	
		System.out.print("<--------");
		
		
		System.out.println("\n Left spacce ");
	
		
		System.out.printf("-------------->%15s",str);
	
		System.out.print("<--------");
		
	}
}
